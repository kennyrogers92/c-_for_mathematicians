#include <iostream>
using namespace std;

int main ()
{
     float b=0;
     int d=0;

     cout << " 0/b = " << 0/b << endl;
     cout << " 0/d = " << 0./d << endl;
     cout << " 1/b = " << 0/b << endl;
     cout << " 1/d = " << 0./d << endl;
     cout << " b/d = " << b/d << endl;
     cout << " d/b = " << d/b << endl;
     return 0;
}
